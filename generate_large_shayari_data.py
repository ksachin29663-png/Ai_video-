# शायरी जनरेट करने का फंक्शन
def generate_shayari(num_shayari=5000): # 5000 शायरियाँ जनरेट करेंगे, जो काफी होंगी
    all_generated_shayari = []
    for i in range(num_shayari):
        category = random.choice(list(shayari_templates.keys()))
        template = random.choice(shayari_templates[category])
        author = random.choice(authors)
        
        # शायरी को थोड़ा यूनीक बनाने के लिए कुछ बदलाव
        unique_content = f"{template.strip()} (ID: {i+1})"
        
        all_generated_shayari.append({
            "category": category,
            "content": unique_content,
            "author": author
        })
    return all_generated_shayari

